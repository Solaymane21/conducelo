package com.example.demo.dto.response;

/**
 * data transfer object representing a car response with owner information.
 */
public class CarResponse {

    private Long id;
    private String brand;
    private String model;
    private Integer year;
    private String color;
    private Double pricePerDay;
    private String location;
    private String description;
    private Boolean isActive;
    private String ownerUsername;
    private String ownerEmail;

    public CarResponse() {
    }

    public CarResponse(Long id, String brand, String model, Integer year, String color, Double pricePerDay, String location, String description, Boolean isActive, String ownerUsername, String ownerEmail) {
        this.id = id;
        this.brand = brand;
        this.model = model;
        this.year = year;
        this.color = color;
        this.pricePerDay = pricePerDay;
        this.location = location;
        this.description = description;
        this.isActive = isActive;
        this.ownerUsername = ownerUsername;
        this.ownerEmail = ownerEmail;
    }

    // getters and setters

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public Integer getYear() {
        return year;
    }

    public void setYear(Integer year) {
        this.year = year;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public Double getPricePerDay() {
        return pricePerDay;
    }

    public void setPricePerDay(Double pricePerDay) {
        this.pricePerDay = pricePerDay;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Boolean getIsActive() {
        return isActive;
    }

    public void setIsActive(Boolean isActive) {
        this.isActive = isActive;
    }

    public String getOwnerUsername() {
        return ownerUsername;
    }

    public void setOwnerUsername(String ownerUsername) {
        this.ownerUsername = ownerUsername;
    }

    public String getOwnerEmail() {
        return ownerEmail;
    }

    public void setOwnerEmail(String ownerEmail) {
        this.ownerEmail = ownerEmail;
    }
}
